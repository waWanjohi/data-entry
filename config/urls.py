from django.contrib import admin
from django.urls import path, include, re_path
from rest_framework import permissions
from drf_yasg.views import get_schema_view
from drf_yasg import openapi

schema_view = get_schema_view(
    openapi.Info(
        title="Data Entry API",
        default_version='v1',
        description="This is a RESTful API for a multiuser data entry application. The API allows for multiple categories of data, including Health Institutions, Professional Details, and Event Details, with the option to a Custom Category where necessary.",
        terms_of_service="https://github.com/waWanjohi/data-entry",
        contact=openapi.Contact(email="gideon@wanjohi.codes"),
        license=openapi.License(name="BSD License"),
    ),
    public=True,
    permission_classes=(permissions.AllowAny,),
)
urlpatterns = [
    path('swagger/', schema_view.with_ui('swagger', cache_timeout=0), name='schema-swagger-ui'),
    path('docs/', schema_view.with_ui('redoc', cache_timeout=0), name='schema-redoc'),
    path('admin/', admin.site.urls),

    path('api/v1/entries/', include('data_entry.urls')),
    path('api/v1/accounts/', include('accounts.urls'))
]
